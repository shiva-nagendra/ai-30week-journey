# ## What is Classification?
# Classification is a type of supervised learning where the model predicts a category or label
# instead of a numeric value.

# ## Difference between Regression and Classification
# - Regression predicts continuous numbers (e.g., house price)
# - Classification predicts discrete labels (e.g., churn: yes/no)

# ## Examples of Classification
# - Customer churn prediction
# - Spam vs non-spam email detection
# - Disease present or not present

# ## Key Idea
# The model often predicts probabilities internally, which are then converted into class labels
# using a decision threshold.

# ## Takeaway
# Classification answers the question: "Which class does this data point belong to?"

