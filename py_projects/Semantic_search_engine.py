# Semantic search engine(Top-k retrieval)

from sentence_transformers import SentenceTransformer
from sklearn.metrics.pairwise import cosine_similarity
import numpy as np

#Load model
model = SentenceTransformer("all-MiniLM-L6-v2")

#Knowledge base
documents = [
     "Artificial intelligence is transforming healthcare.",
    "Machine learning helps analyze large datasets.",
    "Space exploration requires advanced rocket science.",
    "Deep learning improves image recognition systems.",
    "Cooking requires proper heat and ingredients.",
    "Neural networks are inspired by the human brain.",
    "Data science combines statistics and programming.",
    "Airplanes fly using lift generated by wings.",
    "Medicine helps cure diseases and improve health.",
    "Programming is essential for building software."
]

# Convert docs into embeddings
doc_embeddings = model.encode(documents)

#Function to search
def semantic_search(query, top_k=3):
    query_embeddings = model.encode([query])

    scores = cosine_similarity(query_embeddings, doc_embeddings)[0]

    #top_k indices
    top_indices = np.argsort(scores)[::-1][:top_k]

